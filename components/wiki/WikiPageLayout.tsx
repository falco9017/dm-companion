'use client'

import { useState } from 'react'
import { WikiEntryType } from '@prisma/client'
import WikiSidebar from './WikiSidebar'
import WikiEntryEditor from './WikiEntryEditor'
import SettingsModal from './SettingsModal'
import AudioUploadModal from './AudioUploadModal'
import WikiEntryForm from './WikiEntryForm'
import ChatPopup from '@/components/chat/ChatPopup'
import UpdateWikiModal from './UpdateWikiModal'

interface WikiTreeEntry {
  id: string
  title: string
  slug: string
  type: WikiEntryType
  parentId: string | null
  createdAt: Date
}

interface ActiveEntry {
  id: string
  title: string
  type: WikiEntryType
  content: string
  tags: string[]
  isAutoGenerated: boolean
  audioFile: { id: string; filename: string } | null
  children: { id: string; title: string; excerpt: string | null }[]
  createdAt: Date
  updatedAt: Date
}

interface WikiPageLayoutProps {
  campaignId: string
  userId: string
  campaign: {
    name: string
    description: string | null
    language: string
  }
  wikiTree: WikiTreeEntry[]
  activeEntry: ActiveEntry | null
}

export default function WikiPageLayout({
  campaignId,
  userId,
  campaign,
  wikiTree,
  activeEntry,
}: WikiPageLayoutProps) {
  const [settingsOpen, setSettingsOpen] = useState(false)
  const [uploadOpen, setUploadOpen] = useState(false)
  const [createOpen, setCreateOpen] = useState(false)
  const [updateWikiOpen, setUpdateWikiOpen] = useState(false)

  return (
    <div className="flex h-[calc(100vh-4rem)] overflow-hidden">
      <WikiSidebar
        campaignId={campaignId}
        campaignName={campaign.name}
        entries={wikiTree}
        activeEntryId={activeEntry?.id}
        onSettingsClick={() => setSettingsOpen(true)}
        onUploadClick={() => setUploadOpen(true)}
        onCreateClick={() => setCreateOpen(true)}
        onUpdateWikiClick={() => setUpdateWikiOpen(true)}
      />

      {/* Main content */}
      {activeEntry ? (
        <WikiEntryEditor
          campaignId={campaignId}
          userId={userId}
          entry={activeEntry}
        />
      ) : (
        <div className="flex-1 flex items-center justify-center">
          <div className="text-center">
            <p className="text-gray-400 text-lg mb-2">
              {wikiTree.length === 0
                ? 'No wiki entries yet'
                : 'Select an entry from the sidebar'}
            </p>
            <p className="text-gray-500 text-sm">
              Upload audio to generate entries automatically, or create one manually
            </p>
            <div className="mt-4 flex justify-center gap-3">
              <button
                onClick={() => setUploadOpen(true)}
                className="text-sm px-4 py-2 rounded-lg bg-gray-800 text-gray-300 hover:bg-gray-700 transition-colors"
              >
                Upload Audio
              </button>
              <button
                onClick={() => setCreateOpen(true)}
                className="text-sm px-4 py-2 rounded-lg bg-blue-600 text-white hover:bg-blue-700 transition-colors"
              >
                New Page
              </button>
            </div>
          </div>
        </div>
      )}

      {/* Chat popup */}
      <ChatPopup campaignId={campaignId} />

      {/* Modals */}
      <SettingsModal
        campaignId={campaignId}
        userId={userId}
        campaign={campaign}
        isOpen={settingsOpen}
        onClose={() => setSettingsOpen(false)}
      />

      <AudioUploadModal
        campaignId={campaignId}
        isOpen={uploadOpen}
        onClose={() => setUploadOpen(false)}
      />

      <UpdateWikiModal
        campaignId={campaignId}
        isOpen={updateWikiOpen}
        onClose={() => setUpdateWikiOpen(false)}
      />

      {/* Create entry modal */}
      {createOpen && (
        <div className="fixed inset-0 z-50 flex items-center justify-center">
          <div className="absolute inset-0 bg-black/60" onClick={() => setCreateOpen(false)} />
          <div className="relative bg-gray-900 border border-gray-700 rounded-xl w-full max-w-lg mx-4">
            <div className="flex items-center justify-between p-6 border-b border-gray-800">
              <h2 className="text-xl font-bold text-white">Create Wiki Entry</h2>
              <button
                onClick={() => setCreateOpen(false)}
                className="text-gray-400 hover:text-white text-xl"
              >
                &times;
              </button>
            </div>
            <div className="p-6">
              <WikiEntryForm
                campaignId={campaignId}
                userId={userId}
                onDone={() => setCreateOpen(false)}
              />
            </div>
          </div>
        </div>
      )}
    </div>
  )
}
