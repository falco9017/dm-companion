import { auth } from '@/lib/auth'
import { getCampaign, getCampaigns } from '@/actions/campaigns'
import { getWikiTree, getWikiEntry, getPlayerWikiTree } from '@/actions/wiki'
import { getUserProfile } from '@/actions/profile'
import { getEffectiveTier, getLimits } from '@/lib/subscription'
import { getPlayerCharacterSheet } from '@/lib/permissions'
import { getCampaignCharacterSheets } from '@/actions/character-sheet'
import { getUnclaimedCharacters } from '@/actions/campaign-members'
import { notFound } from 'next/navigation'
import WikiPageLayout from '@/components/wiki/WikiPageLayout'

export default async function CampaignPage({
  params,
  searchParams,
}: {
  params: Promise<{ campaignId: string }>
  searchParams: Promise<{ entry?: string; tab?: string }>
}) {
  const { campaignId } = await params
  const { entry: entryParam, tab } = await searchParams
  const session = await auth()
  const userId = session!.user.id

  const [campaign, profile] = await Promise.all([
    getCampaign(campaignId, userId),
    getUserProfile(userId),
  ])

  if (!campaign) {
    notFound()
  }

  const userRole: 'DM' | 'PLAYER' = campaign.ownerId === userId ? 'DM' : 'PLAYER'

  if (userRole === 'DM') {
    const [wikiTree, tier, allCampaigns] = await Promise.all([
      getWikiTree(campaignId, userId),
      getEffectiveTier(userId),
      getCampaigns(userId),
    ])

    const limits = getLimits(tier)
    const isLocked = limits.maxCampaigns !== Infinity && allCampaigns.length > limits.maxCampaigns

    let activeEntry = null
    let activeEntryId = entryParam

    if (!activeEntryId && wikiTree.length > 0) {
      if (tab === 'wiki') {
        const wikiEntry = wikiTree.find((e) => e.type !== 'SESSION_RECAP')
        activeEntryId = wikiEntry?.id
      } else {
        const sessionRecap = wikiTree.find((e) => e.type === 'SESSION_RECAP')
        activeEntryId = sessionRecap?.id
      }
    }

    if (activeEntryId) {
      try {
        activeEntry = await getWikiEntry(activeEntryId, userId)
      } catch {
        // Entry not found, show empty state
      }
    }

    return (
      <WikiPageLayout
        campaignId={campaignId}
        userId={userId}
        userRole="DM"
        campaign={{
          name: campaign.name,
          description: campaign.description,
          language: campaign.language,
          inviteCode: campaign.inviteCode,
        }}
        wikiTree={wikiTree}
        isLocked={isLocked}
        activeEntry={
          activeEntry
            ? {
                id: activeEntry.id,
                title: activeEntry.title,
                type: activeEntry.type,
                content: activeEntry.content,
                tags: activeEntry.tags,
                isAutoGenerated: activeEntry.isAutoGenerated,
                audioFile: activeEntry.audioFile,
                children: activeEntry.children.map((c) => ({
                  id: c.id,
                  title: c.title,
                  excerpt: c.excerpt,
                })),
                createdAt: activeEntry.createdAt,
                updatedAt: activeEntry.updatedAt,
                characterSheet: activeEntry.characterSheet
                  ? {
                      id: activeEntry.characterSheet.id,
                      data: activeEntry.characterSheet.data as unknown as import('@/types/character-sheet').CharacterSheetData,
                      pdfBlobUrl: activeEntry.characterSheet.pdfBlobUrl,
                    }
                  : null,
              }
            : null
        }
        dateFormat={profile.dateFormat}
      />
    )
  }

  // PLAYER view
  const [sessionTree, playerCharacterSheet, partySheets, unclaimedChars] = await Promise.all([
    getPlayerWikiTree(campaignId, userId),
    getPlayerCharacterSheet(campaignId, userId),
    getCampaignCharacterSheets(campaignId, userId),
    getUnclaimedCharacters(campaignId, userId),
  ])

  let activeEntry = null
  let activeEntryId = entryParam

  if (!activeEntryId && sessionTree.length > 0) {
    activeEntryId = sessionTree[0].id
  }

  if (activeEntryId) {
    try {
      // Players can only view SESSION_RECAP entries here
      const found = sessionTree.find((e) => e.id === activeEntryId)
      if (found) {
        const entry = await getWikiEntry(activeEntryId, userId).catch(() => null)
        if (entry && entry.type === 'SESSION_RECAP') {
          activeEntry = entry
        }
      }
    } catch {
      // Entry not found
    }
  }

  return (
    <WikiPageLayout
      campaignId={campaignId}
      userId={userId}
      userRole="PLAYER"
      campaign={{
        name: campaign.name,
        description: campaign.description,
        language: campaign.language,
        inviteCode: null,
      }}
      wikiTree={sessionTree}
      isLocked={false}
      activeEntry={
        activeEntry
          ? {
              id: activeEntry.id,
              title: activeEntry.title,
              type: activeEntry.type,
              content: activeEntry.content,
              tags: activeEntry.tags,
              isAutoGenerated: activeEntry.isAutoGenerated,
              audioFile: activeEntry.audioFile,
              children: activeEntry.children.map((c) => ({
                id: c.id,
                title: c.title,
                excerpt: c.excerpt,
              })),
              createdAt: activeEntry.createdAt,
              updatedAt: activeEntry.updatedAt,
              characterSheet: null,
            }
          : null
      }
      dateFormat={profile.dateFormat}
      playerCharacterSheet={
        playerCharacterSheet
          ? {
              id: playerCharacterSheet.id,
              data: playerCharacterSheet.data as unknown as import('@/types/character-sheet').CharacterSheetData,
              pdfBlobUrl: playerCharacterSheet.pdfBlobUrl,
              wikiEntryId: playerCharacterSheet.wikiEntryId,
            }
          : null
      }
      partySheets={partySheets.map((s) => ({
        id: s.id,
        wikiEntryId: s.wikiEntryId,
        data: s.data as unknown as import('@/types/character-sheet').CharacterSheetData,
        pdfBlobUrl: s.pdfBlobUrl,
        wikiEntry: s.wikiEntry,
        assignedPlayer: s.assignedPlayer,
        assignedPlayerId: s.assignedPlayerId,
      }))}
      unclaimedCharacters={unclaimedChars.map((e) => ({
        id: e.id,
        title: e.title,
        characterSheet: e.characterSheet
          ? { id: e.characterSheet.id, assignedPlayerId: e.characterSheet.assignedPlayerId }
          : null,
      }))}
    />
  )
}
